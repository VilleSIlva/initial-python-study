def saudacao(nome):
    """
    Retorna uma mensagem de saudação personalizada
    
    Args:
        nome (str): Nome da pessoa a ser saudada
    
    Returns:
        str: Mensagem de saudação
    """
    return f"Olá {nome}"


def somar(n1, n2):
    """
    Soma dois números
    
    Args:
        n1 (int/float): Primeiro número
        n2 (int/float): Segundo número
    
    Returns:
        int/float: Resultado da soma
    """
    return n1 + n2